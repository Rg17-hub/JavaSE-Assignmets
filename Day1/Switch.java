//2) Using switch ….case  display whether character is vowel or not.


public class Switch
{
	public static void main(String [] args)
	{
		char ch='G';
		
		switch(ch)
		{
			case 'A':
				System.out.println("Vowel");
				break;	

			case 'E':
				System.out.println("Vowel");
				break;	
			case 'I':
				System.out.println("Vowel");
				break;	

			case 'O':
				System.out.println("Vowel");
				break;	
			case 'U':
				System.out.println("Vowel");
				break;	
			
			default:
				System.out.println("Consonant");
			
		}
	}
}